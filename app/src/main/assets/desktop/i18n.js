/* Current UI language: stored preference -> navigator 2-letter match -> "en". */
function currentLang() {
  var stored = null;
  try { stored = localStorage.getItem("rumbLang"); } catch (e) {}
  if (stored && I18N[stored]) return stored;
  var nav = ((navigator.language || "en").slice(0, 2)).toLowerCase();
  if (I18N[nav]) return nav;
  return "en";
}

/* Translate a key: current lang -> English fallback -> the key itself. */
function t(key) {
  var l = currentLang();
  return (I18N[l] && I18N[l][key]) || I18N.en[key] || key;
}

/* Persist a language choice and re-render by reloading. */
function setLang(code) {
  try { localStorage.setItem("rumbLang", code); } catch (e) {}
  location.reload();
}

/* Reflect the active language on <html> and wire the language picker. */
(function initLangPicker() {
  try { document.documentElement.lang = currentLang(); } catch (e) {}
  var sel = document.getElementById("langSelect");
  if (!sel) return;
  var names = { ca: "Català", en: "English", es: "Español", fr: "Français", de: "Deutsch", it: "Italiano", pt: "Português", nl: "Nederlands", pl: "Polski", ru: "Русский", uk: "Українська", tr: "Türkçe", sv: "Svenska", da: "Dansk", fi: "Suomi", cs: "Čeština", el: "Ελληνικά", ro: "Română" };
  var cur = currentLang();
  Object.keys(I18N).forEach(function (code) {
    var o = document.createElement("option");
    o.value = code;
    o.textContent = names[code] || code;
    if (code === cur) o.selected = true;
    sel.appendChild(o);
  });
  sel.addEventListener("change", function () { setLang(this.value); });
})();
